char	*ft_strchr(const char *s, int c)
{
	int j;

	j = 0;
	while (s[j])
		j++;
	if (c == '\0')
	{
		while (*s != '\0')
			s++;
		return ((char *)s);
	}
	while (j--)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	return (0);
}
