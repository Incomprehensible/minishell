char	*ft_strrchr(const char *s, int c)
{
	int j;

	j = 0;
	while (s[j])
		j++;
	if (c == '\0')
	{
		while (s[j] != c)
			j++;
		return ((char *)&s[j]);
	}
	while (j >= 0)
	{
		if (s[j] == c)
			return ((char *)&s[j]);
		j--;
	}
	return (0);
}
